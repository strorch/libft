#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != 0 && s[i] != (char)c)
		i++;
	if (s[i] == 0 && s[i] != (char)c)
		return (0);
	return (char *)(s + i);
}
